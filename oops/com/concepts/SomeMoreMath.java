package oops.com.concepts;

public class SomeMoreMath {

	public static void main(String[] args) {
		double x = 365.999856;
		
		//Getting absolute value
		System.out.println("Absolute value of x is "+ Math.abs(x));
		
		// Rounding off
		System.out.println("Round of value of x is "+ Math.round(x));
		
		//Finding cuberoot
		int a = 125;
		System.out.println("Cube root of a is "+ Math.cbrt(a));

	}

}
