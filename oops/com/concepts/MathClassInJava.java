package oops.com.concepts;

public class MathClassInJava {

	public static void main(String[] args) {
		double x = 399;
		double y = 249;
		
		// Finding max
		System.out.println("Maximum between x and y is "+ Math.max(x, y));
		
		// Finding min
		System.out.println("Minimum between x and y is "+ Math.min(x, y));
		
		// Finding Square root
		System.out.println("Square root of x is "+ Math.sqrt(x));
		
		//Finding power
		int a = 5;
		int b = 3;
		System.out.println("Power of a to b is "+ Math.pow(a, b));

	}

}
